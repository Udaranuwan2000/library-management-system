    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Connection con = database.connection();
        
        try{
            
            PreparedStatement ptst = con.prepareStatement("insert into library (Name,Password,Email,Contact) values(?,?,?,?)");
            ptst.setString(1, txtName.getText());
            ptst.setString(2, txtPassword.getText());
            ptst.setString(3, txtEmail.getText());
            ptst.setString(4, txtContact.getText());
            
            int i = ptst.executeUpdate();
            
            if(i != 0){
                JOptionPane.showMessageDialog(null, "Librarian Added Successfully !");
                clear();
            }
        
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

